            <h3>Sambo Matsogi <small>Three Step Sparring</small></h3>

            <p class="larger-font">
            <b> Number 2</b>
            Attacker lines up as for number one, then steps back in to a left walking stance, low obverse
            knifehand block. <br><br>

            <b>Attacker:</b> steps forward performing a low right front snap kick, landing in walking stance,
            twin low section block.<br>

            <b>Defender:</b> steps backwards to form a left walking stance whilst performing a low obverse
            knifehand block <br>

            <b>Attacker:</b> steps forward performing a low left front snap kick, landing in walking stance, twin
            low section block.<br>

            <b>Defender:</b> steps backwards to form a right walking stance whilst performing a low knifehand
            block. <br>

            <b>Attacker:</b> steps forward performing a low right front snap kick, landing in walking stance,
            twin low section block. <br>

            <b>Defender:</b> steps backwards to form a left walking stance whilst performing a low obverse
            knifehand block. Counter with a low front snap kick from the front leg, put the kicking leg
            behind you into a left L stance guarding block. Kihap on the kick. <br>
            </p>
