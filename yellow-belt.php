            <h3>Theory <a href="theory/Theory8.pdf" class="pdf" target="_blank"><small>Download Theory</small></a></h3>
            <p class="larger-font">
              Note: You may be asked questions on lower grade theory, so make sure you know it.<br> <br>
            </p>

            <b> Green: </b> Signifies the plant’s growth as Taekwondo skills begin to develop. <br><br>

            <b> Three-step sparring (Sambo Matsoki): </b> Is practised to improve focus, distance,
            and timing. <br><br>

            <b> Punches (Jirugi) and strikes (Taerigi): </b> Can be used against hard or soft targets,
            their aim is to smash or pierce. This is achieved by focussing the elements of power (including breathing and dynamic twist) to a single point of execution
            at the end of the technique. <br><br>

            <b> Thrusts (Tulgi): </b>  Can only be used against soft targets, their aim is to penetrate
            into the target area. This is achieved using ‘sharp’ attacking tools, in tension throughout execution, so the technique is effective anywhere along the line of attack. <br><br>

            <b> Korean numerical system: </b> Is base 10, as in most modern systems. As in English, two digit numbers are named by following the number of ‘tens’ with the number of ‘units’. e.g. <b>11</b>: Yul hana, <b>27</b>: Sumul ilgop


