            <h3>Ilbo Matsogi <small>One Step Sparring</small></h3>

            <p class="larger-font">
              <b>Number 9</b><br><br>
              Slide back on 45 degree angle with the right leg to form a right L-stance whilst
              performing a forearm guarding block.<br><br>

              Counter with a high flying front kick to the head with the right leg, landing in Left Lstance
              forearm guarding block.<br><br>

              Swap for left attack.<br><br>

              <b>Number 10</b><br><br>
              Slide back on 45 degree angle with the right leg to form a right L-stance whilst
              performing a forearm guarding block.<br><br>

              Counter with a high flying side piercing kick to the head with the right leg, landing in Left L-Stance guarding block<br><br>

              Swap for a left attack.<br><br>
            </p>

            <h3>Ilbo Jayu Matsogi <small>One Step Semi-Free Sparring</small></h3>
            <p class="larger-font">
              <b>General Points</b> <br><br>
              The attacker lines up to the defender in the same way as three step sparring, and they
              go back into a left walking stance low block.<br><br>

              When he or she is ready to attack, the attacker must shout and wait for the defender
              to respond with a shout before the attack can begin.<br><br>

              This is only done on one side, i.e. not like one step sparring with both right and left
              sides.<br><br>

              <b> Number 1</b> <br><br>
              <b>Attacker:</b> steps forward into a right walking stance, high obverse punch.<br><br>

              <b>Defender:</b> step back into left walking stance high outer forearm block, followed by a front
              snap kick from the back leg, making sure you ‘fall’ forward after the kick into a walking stance
              guarding block.<br><br>

              <b>Attacker:</b> not moving the feet, perform a low reverse outer forearm block, then counter
              with a low front snap kick from the front leg to the lower abdomen, placing the kicking leg
              behind you into a right L-stance guarding block. Kihap on the kick.<br><br>

              <b> Number 2 </b>
              <b>Attacker:</b> steps forward into a right walking stance, high obverse punch.<br><br>

              <b>Defender:</b> step back into left walking stance high double forearm block, followed by reverse
              high flat fingertip thrust.<br><br>

              <b>Attacker:</b> flying turning kick with the right leg to the solar plexus while moving to the left,
              land left L-stance forearm guarding block. Kihap on the kick.<br><br>
            </p>
