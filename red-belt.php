            <h3>Ilbo Matsogi <small>One Step Sparring</small></h3>

            <p class="larger-font">
              <b>Number 11</b><br><br>
              Slide back on 45 degree angle with the right leg to form a right L-stance whilst
              performing a forearm guarding block.<br><br>

              Counter with a high flying turning kick to the head with the right leg, landing in Left Lstance
              forearm guarding block.<br><br>

              Swap for left attack.<br><br>

              <b>Number 12</b><br><br>
               Slide back on 45 degree angle with the right leg to form a right L-stance whilst
               performing a forearm guarding block.<br><br>
               
               Counter with a high flying reverse turning kick to the head with the right leg, landing
               in Right L-stance forearm guarding block.<br><br>

               Swap for a left attack.<br><br>
            </p>

            <h3>Ilbo Jayu Matsogi <small>One Step Semi-Free Sparring</small></h3>
            <p class="larger-font">

              <b> Number 3</b> <br><br>
              <b>Attacker:</b> steps forward low front snap kick with the right leg.<br><br>

              <b>Defender:</b> step back into left walking stance performing an x-fist pressing block followed by
              a middle reverse punch with the right hand.<br><br>

              <b>Attacker:</b> before you land the kicking leg, block with a reverse outer forearm block quickly
              followed by a high obverse punch with the right hand as the right foot hits the ground. Kihap
              on the punch.<br><br>

              <b> Number 4 </b> <br><br>
              <b>Attacker:</b> steps forward middle side piercing kick from the back (right) leg. Land in sitting
              stance and perform a back elbow thrust with the left elbow.<br><br>

              <b>Defender:</b> assume a left L-stance while dodging to the outside of the kick, performing an
              outwards knifehand strike to the back on the neck.<br><br>

              <b>Defender:</b> step forward with your right foot then left foot (double stepping), pivot on the
              right and perform a high turning kick with the left leg. Kihap on the kick<br><br>

              <b> Number 5</b><br><br>
              <b>Attacker:</b> steps forward into a right fixed stance middle forefist side punch.<br><br>

              <b>Defender:</b> Assume a left rear foot stance forearm guarding block. Pivot on the right foot
              executing a high reverse turning kick with the left leg.<br><br>

              <b>Attacker:</b> execute a high grasping block. Step your left leg behind your right into an xstance
              while still holding the leg. This should push your hips through the opponent causing
              him or her to fall to the floor. Direct the leg aside and perform a low side piercing kick with
              the right leg to the groin. <br><br>
            </p>
